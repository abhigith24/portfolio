/**
 * Renders a Lucide icon by its PascalCase name.
 * Returns null if the icon name is not found in the registry.
 */
export function getIconByName(
  iconName: string,
  props: { size?: number; className?: string } = {}
): React.ReactNode {
  const Icon = ICON_MAP[iconName];
  if (!Icon) return null;
  return <Icon {...props} />;
}
